package untref_tfi.pkg_ActiveContours.domain;


public class TemporalColorDouble {

	private double red;
	private double green;
	private double blue;

	public TemporalColorDouble(double red, double green, double blue) {
		this.red = red;
		this.green = green;
		this.blue = blue;
	}

	public double getRed() {
		return red;
	}

	public double getGreen() {
		return green;
	}

	public double getBlue() {
		return blue;
	}


}
